package eg00123;

public class AutoboxingAndUnboxingDemo {

	public static void main(String[] args) {

		// Primitive to Object
		Integer i1 = new Integer(10);

		int i2 = i1;

		System.out.println("i1 : " + i1);
		System.out.println("i2 : " + i2);

		//
		Character ch1 = 'a';

		char ch2 = ch1;

		System.out.println("ch1 : " + ch1);
		System.out.println("ch2 : " + ch2);

	}

}
